class Rectangle:

    def __init__(self, width, height=None):
        self.width = width
        if height is None:
            self.height = width
        else:
            self.height = height

    def perimeter(self):
        return 2 * (self.width + self.height)

    def area(self):
        return self.width * self.height


spam = Rectangle(5, 2)
eggs = Rectangle(7)

print(f'{spam.area() =}')
print(f'{spam.perimeter()= }')
print(f'{eggs.area()= }')
print(f'{eggs.perimeter()= }')
